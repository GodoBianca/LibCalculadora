package calculator;

public class FoundationCalculator {
    public int sumTwoNums(int a, int b) {
        return a + b;
    }
    public int subtractTwoNums(int a, int b) {
        return a - b;
    }
    public int divideTwoNums(int a, int b) {
        return a / b;
    }
    public int multiplyTwoNums(int a, int b) {
        return a * b;
    }
}
